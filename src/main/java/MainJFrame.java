    private void doneEditFTE_Pressed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneEditFTE_Pressed
        // TODO add your handling code here:
        // Read the user-provided attribute info for the employee and add the employee to the hash table.

        String strENumber = editEmpNumFTE.getText();
        int eNumber = Integer.parseInt(strENumber);
        theEmployee = theHT.searchEmployee(eNumber);
        // Should check that this employee number does not already exist for an employee already in the hash table.

        String fName = editFirstNameFTE.getText();
        String lName = editLastNameFTE.getText();
        String gender = editGenderFTE.getText();
        String dedRate = editDeductRateFTE.getText();
        Double deductRate = Double.parseDouble(dedRate);
        String salary = editYearlySalaryFTE.getText();
        Double yearlySalary = Double.parseDouble(salary);

        FTE theFTE = new FTE(eNumber, fName, lName, gender, 0, deductRate, yearlySalary);
        
        theHT.removeEmployee(eNumber);
        
        theHT.addEmployee(theFTE);
        
        editEMPtextField.setText("Enter Employee Number");
        
        editFTEjDialog.dispose();
        
    }//GEN-LAST:event_doneEditFTE_Pressed

